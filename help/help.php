  <h3 id="thepanels">The Panels:</h4>
  <p>You can close any panel  with the ESC button on your keyboard.</p>
  <p>If you press the space  key on your keyboard you will enable the mapview</p>
  
  		<h4 id="create">create:</h4>
  		<p>The create panel  gives you the possibility to create a place. As soon as it is opened,  
  		   a marker appears which can be set at the desired spot. After this,  you can fill in the 
  		   form and mark which categories are suitable to  give a better description of the place.
  		   You can also upload pictures,  and give a video url, in order to attach a video. 
  		   We are aware, that it can be  difficult to categorize some places in food, accomodation,  
  		   discussions, learning, entertainment, stuff and direct actions. Still, we disscused this 
  		   categorization many times and optimised it  and did our best to make it minimal and functional 
  		   at the same time.  Still we are open and thankful for any ideas.
 		   If you add a place,  try to think how it could be somehow categorized in the given way.  
 		   This will simply help to find the place better.The description  text should show and explain 
 		   the place as it is and how it can be  used, accessed and found(maybe you need to climb, crawl, etc.). 
 		   Try  to express the rules (i.e you should behave quiet, make no fire, take  your garbage, etc). 
 		   Maybe there are events taking place.</p>
  		   
  		  
  
  
          <p>The voting system, is there to give a personal impression of the place.  
          We understand that in some cases it seems rather unnice. 
          Rating a  social center is may not be the proper thing to do over such a star  system, 
          still though it could be nice in some other cases such as  accommodation, or stuff for free.</p>




		<h4 id="edit">edit panel:</h4>
      	<p>The edit panel  gives you the possibility to edit all entrances. This means you can  change/add pictures and 
