  <h3>Welcome to collectopia!</h3>
   
  <h4>What is collectopia?</h4>
  <p>Collectopia is the result of trying to answer the question if there is a possibility to move in a no budget 
  way and how far do you get. We do not seek for the ultimative way to get through the world without paying anything, 
  rather we where interested how can the places we have in our enviroment used in a different way that satisfies our needs. 
  Our idea is to map the spaces that are controled by ownership, restrictions and policies and give them a chance to become 
  something new. This is not a call for illegal activities, furthermore it is the call for reusing our enviroment in a 
  social way.</p>
  
  <p>The site aims to  function as an open platform to map collectives, places that can be used public and common spaces  
   worldwide which can be used in a no budget  and free way. The basic idea behind collectopia is to map self-organised  
   places that people created or found all over the world, which are  free and open to the public. But it should not 
   stop there - In general, collectopia is open to any  kind of place, and no restriction should exclude a free  
   camping/eating/learning/etc possibility. For example, you could  map a park in a city as an accommodation point 
   as well as a squat (if the people there agree) or  a great spot at the country side. A place where you can find 
   food could be from dumpster-diving-spots up to open kitchens or a place with apple trees. What should 
   be always given,  is a  clear explanation how to use the places in the best way. If you mark a place, try to give 
   a objective report about what is going on there and respect the fact that pictures should never show faces. 
   Collectopia offers also the possibility  to search for specific needs such as clothes, accommodation, food,  
   entertainment, language classes, workshops and discussions. </p>
  <p>Overall,  the platform should   give people the opportunity to discover new ideas,
   share knowledge  and learn from each other. Collectopia  should not promote tourism, but should offer a possibility 
   to move  everywhere in an alternative no-budget way. It should also give a  possibility of meeting people in different 
   places, who work in  similar ways and focus on similar aims. And in its best the question of property and creative 
   thinking should be wakened. </p>
  

  <p>Furthermore this map should  work as a guide for people who are going to a  city and want to participate, 
  either in a way of helping  or learning, in a non Consumption way. And it  should be at its best a source of 
  inspiration to set up  similar things in your area.</p>

  <p>The  site is completely open-access and everybody has the same rights in  editing, writing and voting/reviewing.  
  Collectopia is not saving ANY  data from the users and pictures will be allways automatically freed  from any metadata 
  when you upload them.</p>
  
